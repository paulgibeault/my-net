def _check_required(state: dict) -> list[str]:
    """Return list of missing required fields."""
    required = {
        "server_ip": "VPS IP address",
        "server_password": "Server root password",
        "domain": "Domain name",
        "cloudflare_token": "Cloudflare tunnel token",
        "tailscale_auth_key": "Tailscale auth key",
        "admin_password": "Admin password",
        "jwt_secret": "JWT secret (generated)",
        "plc_rotation_key": "PLC rotation key (generated)",
    }
    missing = []
    for key, label in required.items():
        if not state.get(key):
            missing.append(label)
    return missing


def _deploy_portainer(state: dict, repo_root: Path) -> None:
    """Copy portainer compose file to deployment dir and note it in state."""
    portainer_src = repo_root / "compose" / "portainer" / "compose.yml"
    if portainer_src.exists():
        console.print(f"[green]✓ Portainer compose stack ready at {portainer_src}[/green]")
    else:
        console.print(f"[yellow]⚠  Portainer compose file not found at {portainer_src} — skipping[/yellow]")


def _generate_hosts_yml(state: dict, dest: Path) -> None:
    """Write ansible inventory hosts.yml."""
    dest.parent.mkdir(parents=True, exist_ok=True)
    content = f"""# Generated by my-net wizard
# DO NOT EDIT MANUALLY — re-run the wizard to regenerate

all:
  hosts:
    my-net-server:
      ansible_host: {state['server_ip']}
      ansible_user: root
      ansible_ssh_pass: "{{{{ vault_server_ssh_password }}}}"
      ansible_become: yes
  vars:
    mynet_domain: {state['domain']}
    mynet_admin_email: admin@{state['domain']}
"""
    dest.write_text(content)
    console.print(f"[green]✓ Generated {dest}[/green]")


def _generate_vault_yml(state: dict, dest: Path, vault_password: str) -> None:
    """Write and encrypt ansible vault file."""
    dest.parent.mkdir(parents=True, exist_ok=True)

    plaintext = f"""# Encrypted by my-net wizard with ansible-vault
# Decrypt with: ansible-vault decrypt ansible/group_vars/vault.yml

vault_server_ssh_password: "{state['server_password']}"
vault_cloudflare_tunnel_token: "{state['cloudflare_token']}"
vault_tailscale_auth_key: "{state['tailscale_auth_key']}"
vault_admin_password: "{state['admin_password']}"
vault_jwt_secret: "{state['jwt_secret']}"
vault_plc_rotation_key: "{state['plc_rotation_key']}"
"""

    # Write plaintext to temp file, then encrypt in place
    with tempfile.NamedTemporaryFile(mode="w", suffix=".yml", delete=False) as tmp:
        tmp.write(plaintext)
        tmp_path = tmp.name

    with tempfile.NamedTemporaryFile(mode="w", suffix=".pass", delete=False) as pf:
        pf.write(vault_password)
        pass_path = pf.name

    try:
        result = subprocess.run(
            ["ansible-vault", "encrypt", tmp_path, "--vault-password-file", pass_path, "--output", str(dest)],
            capture_output=True,
            text=True,
        )
        if result.returncode != 0:
            # Fallback: write plaintext with warning if ansible-vault fails
            console.print(f"[yellow]⚠ ansible-vault encrypt failed: {result.stderr.strip()}[/yellow]")
            console.print("[yellow]Writing unencrypted vault file — secure your server![/yellow]")
            dest.write_text(plaintext)
        else:
            console.print(f"[green]✓ Generated and encrypted {dest}[/green]")
    finally:
        os.unlink(tmp_path)
        os.unlink(pass_path)


def _stream_ansible(state: dict, repo_root: Path, vault_password: str) -> bool:
    """Run ansible-playbook and stream output. Returns True on success."""
    playbook = repo_root / "ansible" / "playbooks" / "provision-node.yml"
    inventory = repo_root / "ansible" / "inventory" / "hosts.yml"

    if not playbook.exists():
        rprint(f"[red]Playbook not found: {playbook}[/red]")
        rprint("[yellow]Make sure you're running from the my-net repo directory.[/yellow]")
        return False

    with tempfile.NamedTemporaryFile(mode="w", suffix=".pass", delete=False) as pf:
        pf.write(vault_password)
        pass_path = pf.name

    console.print()
    console.print(Rule("[bold cyan]Running Ansible Playbook[/bold cyan]"))
    console.print()

    cmd = [
        "ansible-playbook",
        str(playbook),
        "-i", str(inventory),
        "--vault-password-file", pass_path,
        "--diff",
    ]

    try:
        process = subprocess.Popen(
            cmd,
            stdout=subprocess.PIPE,
            stderr=subprocess.STDOUT,
            text=True,
            bufsize=1,
        )

        for line in process.stdout:
            line = line.rstrip()
            if line.startswith("TASK"):
                console.print(f"[bold cyan]{line}[/bold cyan]")
            elif "ok:" in line or "changed:" in line:
                console.print(f"[green]{line}[/green]")
            elif "failed:" in line or "FAILED" in line:
                console.print(f"[red]{line}[/red]")
            elif "skipping:" in line:
                console.print(f"[dim]{line}[/dim]")
            elif line.startswith("PLAY RECAP"):
                console.print(f"[bold]{line}[/bold]")
            else:
                console.print(line)

        process.wait()
        return process.returncode == 0

    except FileNotFoundError:
        rprint("[red]ansible-playbook not found — is ansible-core installed?[/red]")
        rprint("[yellow]Try: pip install ansible-core[/yellow]")
        return False
    finally:
        os.unlink(pass_path)


