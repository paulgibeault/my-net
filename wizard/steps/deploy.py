def run(wizard_state: dict) -> dict:
    """Run the deploy step. Returns updated state."""
    _header()
    _show_review(wizard_state)

    # Check for missing required fields
    missing = _check_required(wizard_state)
    if missing:
        console.print(Panel(
            "[red]Missing required configuration:[/red]\n\n" +
            "\n".join(f"  • {m}" for m in missing) +
            "\n\n[yellow]Please re-run the wizard to fill in the missing fields.[/yellow]",
            border_style="red",
            title="[red]Cannot Deploy[/red]",
        ))
        return wizard_state

    console.print(Panel(
        deployment_flow(),
        title="[bold]What happens next[/bold]",
        border_style="dim",
        padding=(0, 1),
    ))
    console.print()

    confirmed = questionary.confirm(
        "Everything look good? Ready to deploy?",
        default=False,
    ).ask()

    if not confirmed:
        rprint("[yellow]No problem — re-run the wizard any time to deploy.[/yellow]")
        return wizard_state

    console.print()
    console.print(Rule("[bold cyan]Starting Deployment[/bold cyan]"))
    console.print()

    # Use repo root for ansible files
    repo_root = _REPO_ROOT

    with console.status("[bold cyan]Generating Ansible inventory...[/bold cyan]", spinner="dots"):
        _generate_hosts_yml(wizard_state, repo_root / "ansible" / "inventory" / "hosts.yml")

    with console.status("[bold cyan]Encrypting secrets with ansible-vault...[/bold cyan]", spinner="dots"):
        _generate_vault_yml(
            wizard_state,
            repo_root / "ansible" / "group_vars" / "vault.yml",
            vault_password=wizard_state["admin_password"],
        )

    _deploy_portainer(wizard_state, repo_root)

    success = _stream_ansible(wizard_state, repo_root, wizard_state["admin_password"])

    if success:
        wizard_state["deployed"] = True
        wstate.mark_step_complete(wizard_state, STEP_NAME)
        _celebrate(wizard_state["domain"], server_ip=wizard_state.get("server_ip", ""))
    else:
        console.print()
        console.print(Panel(
            "[red]Deployment failed.[/red]\n\n"
            "Don't panic — your configuration is saved. Common fixes:\n\n"
            "  • Check that your VPS is reachable (ping it)\n"
            "  • Make sure the Cloudflare tunnel token is correct\n"
            "  • Check for typos in your domain name\n"
            "  • Look at the Ansible output above for specific errors\n\n"
            "[dim]Re-run the wizard to try again. Your answers are saved![/dim]",
            border_style="red",
            title="[red]Deployment Failed[/red]",
            padding=(1, 2),
        ))

    return wizard_state
